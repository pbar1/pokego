// TypeEffectiveness returns the damage multiplier for a given attacking and defending type pair for a given generation
func TypeEffectiveness(gen Generation, attacker, defender DamageType) float64 {
	// this should never happen, so we return a neutral result
	if attacker > TypeFairy || defender > TypeFairy {
		return 1.0
	}
	if gen == Gen1 {
		return TypechartGen1[attacker][defender]
	}
	if gen >= Gen2 && gen < Gen6 {
		return TypechartGen2[attacker][defender]
	}
	if gen >= Gen6 {
		return TypechartGen6[attacker][defender]
	}
	// this should never happen, so we return a neutral result
	return 1.0
}
